    public final void loadData() {
        btn_simpan_diana.setEnabled(true);
        btn_hapus_diana.setEnabled(false);
        btn_edit_diana.setEnabled(false);
        model.getDataVector().removeAllElements();
        model.fireTableDataChanged();
        
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM tbl_lokasi";
            ResultSet r = s.executeQuery(sql);
            
            while (r.next()) {
                Object[] o = new Object[2];
                o[0] = r.getString("kd_lokasi");
                o[1] = r.getString("nama_lokasi");
                model.addRow(o);
            }
            r.close();
            s.close();
        } catch (SQLException e) {
            System.out.println("Terjadi Kesalahan");
        }
    }
    
    public void kode() {
        try {
            Connection c = koneksi.getKoneksi();
            Statement s = c.createStatement();
            
            String sql = "SELECT * FROM tbl_lokasi";
            ResultSet r = s.executeQuery(sql);
            
            if (r.next()) {
                String kdlokasi = r.getString("kd_lokasi").substring(1);
                String AN = "" + (Integer.parseInt(kdlokasi)+1);
                String Nol ="";
                
                if (AN.length()==1) {
                    Nol = "000";
                } else if (AN.length()==2) {
                    Nol = "00";
                } else if (AN.length()==3) {
                    Nol = "0";
                } else if (AN.length()==4) {
                    Nol = "";
                }
            
                kdlokasi_diana.setText("T" + Nol + AN);
            } else {
                kdlokasi_diana.setText("T0001");
            }
        } catch (Exception e) {
            System.out.println("Terjadi Kesalahan");
        }
    }
