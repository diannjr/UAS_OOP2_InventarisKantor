    private void tabel_lokasiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabel_lokasiMouseClicked
        // TODO add your handling code here:
        btn_simpan_diana.setEnabled(false);
        btn_edit_diana.setEnabled(true);
        btn_hapus_diana.setEnabled(true);
        int i = tabel_lokasi.getSelectedRow();

        if (i == -1) {
            return;
        }
        String kdlokasi = (String) model.getValueAt(i, 0);
        kdlokasi_diana.setText(kdlokasi);
        kdlokasi_diana.setEnabled(false);
        
        String nmlokasi = (String) model.getValueAt(i, 1);
        nmlokasi_diana.setText(nmlokasi);
    }//GEN-LAST:event_tabel_lokasiMouseClicked

